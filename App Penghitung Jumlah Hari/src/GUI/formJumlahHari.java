    private void buttonHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHitungActionPerformed
      if (textTahun.getText().isEmpty()){
          JOptionPane.showMessageDialog(null, "Error : Kolom Tahun Kosong!",
                  "Error : Kolom Tahun Kosong!", JOptionPane.WARNING_MESSAGE);
          
      }else{
          int tahun = Integer.parseInt(textTahun.getText());
          int jumlahHari;
          if(ComboBox.getSelectedItem().equals("Februari")){
              if ( (( tahun % 4 == 0) && !(tahun % 100 == 0 ))|| (tahun % 400 == 0))  
               jumlahHari = 29;
        else   jumlahHari = 28;
          } else if (ComboBox.getSelectedItem().equals("April") ||
                  ComboBox.getSelectedItem().equals("Juni") ||
                  ComboBox.getSelectedItem().equals("September") ||
                  ComboBox.getSelectedItem().equals("November")) {
              
                jumlahHari = 31;
              Hari.setText("Jumlah Hari PAda Bulan"+ ComboBox.getSelectedItem()+ "tahun"+ tahun + "adalah"+ jumlahHari);
              
          }else{
               jumlahHari = 30;
          }
      }
    }//GEN-LAST:event_buttonHitungActionPerformed

