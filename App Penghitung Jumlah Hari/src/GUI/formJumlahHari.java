    private void prosesSimpan() {
        try {
            BufferedWriter out = new BufferedWriter (new FileWriter("FormJumlahHari.txt"));
            //simpan file
            out.write(Hari.getText());
            //tampilan dialog bila berhasil
            JOptionPane.showMessageDialog(null, "file Berhasil Disimpan");
            //tutup output
            out.close();
            
        } catch (Exception e){
            System.out.println("File Error!!");
            
        }
    }
      
